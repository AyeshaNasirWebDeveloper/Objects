/* Non Premitive Data Types
Objects */


const student = {
    fullName: "Ayesha Nasir",
    marks: 93,
    grade: "A+",
    isPass: true,
}

console.log(student);

// OR

console.log(student.fullName);
console.log(student.marks);
console.log(student.grade);
console.log(student.isPass);


